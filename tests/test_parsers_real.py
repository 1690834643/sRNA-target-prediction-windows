"""Parser tests against real tool output produced on the bioinformatics server.

These golden files were generated by running the actual binaries on the
example FASTA pair:

  miRanda 3.3a (conda env miRNA)
  RNAhybrid 2.1.2 (conda env miRNA, with -s 3utr_human)
  pita_prediction.pl (PITA pipeline)

Refresh via scripts/build_real_goldens.sh whenever the tool versions change.
"""
from __future__ import annotations

from pathlib import Path

from srna_win_target.core.models import ToolConfig
from srna_win_target.tools.base import RawToolOutput
from srna_win_target.tools.miranda import MirandaRunner
from srna_win_target.tools.pita import PitaRunner
from srna_win_target.tools.rnahybrid import RNAhybridRunner

REAL_GOLDEN = Path(__file__).parent / "golden" / "real"


def _raw(tool_name: str, fixture: Path) -> RawToolOutput:
    return RawToolOutput(
        tool_name=tool_name,
        chunk_id="real0",
        stdout="",
        stderr="",
        output_file=fixture,
        returncode=0,
    )


def test_real_miranda_output_parses_three_hits() -> None:
    fixture = REAL_GOLDEN / "miranda" / "sample.out"
    runner = MirandaRunner(ToolConfig(name="miranda", executable=Path("/bin/true")))
    hits = runner.parse_output(_raw("miranda", fixture))

    assert len(hits) == 3
    first = hits[0]
    assert first.mirna_id == "example_miR_1"
    assert first.target_id.startswith("example_gene_1_3")
    assert first.score == 181.0
    assert first.energy == -29.36
    assert first.start == 5
    assert first.end == 27
    # Strongest hit is example_miR_2 vs example_gene_2 with score 195.0
    best = max(hits, key=lambda h: h.score)
    assert best.score == 195.0
    assert best.energy == -34.23


def test_real_rnahybrid_output_parses_eight_hits() -> None:
    fixture = REAL_GOLDEN / "rnahybrid" / "sample.txt"
    runner = RNAhybridRunner(ToolConfig(name="rnahybrid", executable=Path("/bin/true")))
    hits = runner.parse_output(_raw("rnahybrid", fixture))

    assert len(hits) == 8
    # Most significant hit (smallest pvalue) is example_miR_2 vs example_gene_2.
    best = min(hits, key=lambda h: h.pvalue)
    assert best.mirna_id == "example_miR_2"
    assert best.target_id.startswith("example_gene_2_3")
    assert best.energy == -36.9
    assert best.pvalue < 1e-5
    # Score is None in compact mode — only mfe/pvalue available.
    for h in hits:
        assert h.score is None
        assert h.start is not None


def test_real_pita_output_parses_start_end_columns() -> None:
    fixture = REAL_GOLDEN / "pita" / "sample_pita_results.tab"
    runner = PitaRunner(ToolConfig(name="pita", executable=Path("/bin/true")))
    hits = runner.parse_output(_raw("pita", fixture))

    assert len(hits) == 2
    by_mirna = {h.mirna_id: h for h in hits}
    assert "example_miR_1" in by_mirna and "example_miR_2" in by_mirna

    h1 = by_mirna["example_miR_1"]
    assert h1.target_id == "example_gene_1_3utr"
    assert h1.score == -24.28          # ddG
    assert h1.energy == -26.5          # dGduplex
    assert h1.start == 27              # explicit Start column from pita_prediction.pl
    assert h1.end == 19

    h2 = by_mirna["example_miR_2"]
    assert h2.target_id == "example_gene_2_3utr"
    assert h2.score == -28.07
    assert h2.energy == -30.7
    assert h2.start == 27
    assert h2.end == 19
